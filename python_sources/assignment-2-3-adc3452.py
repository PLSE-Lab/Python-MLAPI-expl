# Use the %timeit command to find the largest array of random floats (numbers between 0 and 1) 
# that can be sorted in 10ms, 100ms, and 1s on your computer.
import random
import time

def timed_floats(interval):
    x = []
    t_end = time.time() + interval
    while time.time() < t_end:
        y = random.uniform(0, 1)
        x.append(y)
        x.sort()
    print(len(x))


