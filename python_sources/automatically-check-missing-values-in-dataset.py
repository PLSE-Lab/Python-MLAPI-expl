X_train = pd.read_csv(os.path.join("../input", 'X_train.csv'))
X_test = pd.read_csv(os.path.join("../input", 'X_test.csv'))
y_train = pd.read_csv(os.path.join("../input", 'y_train.csv'))

def missing_data(data):
    total = data.isnull().sum()
    percent = (data.isnull().sum()/data.isnull().count()*100)
    tt = pd.concat([total, percent], axis=1, keys=['Total', 'Percent'])
    types = []
    for col in data.columns:
        dtype = str(data[col].dtype)
        types.append(dtype)
    tt['Types'] = types
    return(np.transpose(tt))

print(missing_data(X_train))
print(missing_data(X_test))
print(missing_data(y_train))


