f = open('../input/zuckerberg-testimony/mark_testimony.txt')
contents = f.read() # read all content of the file
f.close()


# In[5]:


contents = re.sub('[^A-Za-z0-9]+', ' ', contents.replace('\r', '').replace('\n', ''))
words = contents.split(' ')
words = [w.lower() for w in words if len(w)>1]
words = [w.strip() for w in words]
goodwords = [w for w in words if w not in ENGLISH_STOP_WORDS]
stemmer = PorterStemmer()
#words[:25]
words = [w for w in goodwords]
stemmed = [stemmer.stem(w) for w in words]
ctr = Counter(stemmed)


# In[ ]:


wordcloud = WordCloud()
wordcloud.fit_words(ctr)

fig=plt.figure(figsize=(50, 30))
plt.imshow(wordcloud)
plt.axis("off")
plt.title("Zuckerberg Testimony", fontsize=60)
